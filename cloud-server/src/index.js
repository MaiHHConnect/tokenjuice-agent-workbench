router.patch('/api/scheduler/capacity', (ctx) => {
  const { maxConcurrentAgents } = ctx.request.body
  if (typeof maxConcurrentAgents !== 'number' || maxConcurrentAgents < 1 || maxConcurrentAgents > 20) {
    ctx.status = 400
    ctx.body = { error: '容量必须在 1-20 之间' }
    return
  }
  const previousCapacity = enhancedScheduler.maxConcurrentAgents
  enhancedScheduler.maxConcurrentAgents = maxConcurrentAgents
  // 持久化到数据库
  db.updateSchedulerConfig({ maxConcurrentAgents })

  if (maxConcurrentAgents > previousCapacity) {
    enhancedScheduler.requestImmediateSchedule(`capacity increased ${previousCapacity} -> ${maxConcurrentAgents}`)
  }

  ctx.body = {
    success: true,
    maxConcurrentAgents,
    previousCapacity,
    schedulingTriggered: maxConcurrentAgents > previousCapacity && enhancedScheduler.running
  }
})

