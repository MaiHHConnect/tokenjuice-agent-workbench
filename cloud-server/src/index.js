router.post('/api/dingtalk/callback', async (ctx) => {
  const { conversationType, conversationId, chatbotCorpId, senderNick, senderStaffId, msgId, createAt, text, conversationTitle } = ctx.request.body

  console.log('[DingTalk Callback] Received:', JSON.stringify(ctx.request.body).substring(0, 200))

  // 只处理机器人收到的消息
  if (!chatbotCorpId) {
    ctx.body = { success: true }
    return
  }

  // 解析消息内容
  const content = text?.content || ''
  if (!content.startsWith('/')) {
    // 非命令消息，可以选择忽略或自动回复
    ctx.body = { success: true }
    return
  }

  // 处理命令
  try {
    await dingtalk.handleMessage({
      type: conversationType === '2' ? 'group' : 'private',
      chatId: conversationId,
      userId: senderStaffId,
      userName: senderNick,
      text: content,
      chatName: conversationTitle
    }, `http://localhost:${PORT}`)
  } catch (e) {
    console.error('[DingTalk Command] Error:', e.message)
  }

  ctx.body = { success: true }
})

