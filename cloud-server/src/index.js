function serializeTask(task) {
  return enhancedScheduler.decorateTask(task)
}

function serializeTaskList(tasks = []) {
  return tasks.map(task => serializeTask(task))
}

function serializeBoard(board = {}) {
  return Object.fromEntries(
    Object.entries(board).map(([status, tasks]) => [status, serializeTaskList(tasks)])
  )
}

function serializeCompactSubTask(task) {
  if (!task) return task
  return {
    id: task.id,
    title: task.title,
    status: task.status,
    taskTag: task.taskTag,
    parentTaskId: task.parentTaskId || null,
    assignedAgentId: task.assignedAgentId || null,
    skipAsDependency: Boolean(task.skipAsDependency),
    blockedReason: task.blockedReason || null,
    updatedAt: task.updatedAt || null
  }
}

function truncateText(value, maxLength = 180) {
  const text = String(value || '').replace(/\r/g, '').trim()
  if (!text) return ''
  return text.length > maxLength ? `${text.slice(0, Math.max(0, maxLength - 1))}…` : text
}

function serializeAgentSummary(agent) {
  if (!agent) return agent
  return {
    id: agent.id,
    name: agent.name,
    role: agent.role,
    description: agent.description,
    model: agent.model,
    level: agent.level,
    capabilities: agent.capabilities || [],
    skillMode: agent.skillMode || 'all',
    allowedSkills: agent.allowedSkills || [],
    status: agent.status,
    currentTaskId: agent.currentTaskId || null,
    lastHeartbeat: agent.lastHeartbeat || null,
    createdAt: agent.createdAt || null,
    updatedAt: agent.updatedAt || null,
    sourceLabel: agent.sourceLabel || null
  }
}

function serializeWikiSummary(wiki) {
  if (!wiki) return wiki
  return {
    id: wiki.id,
    title: wiki.title,
    preview: truncateText(wiki.content, 160),
    keywords: wiki.keywords || [],
    taskTag: wiki.taskTag,
    parentTaskId: wiki.parentTaskId || null,
    createdAt: wiki.createdAt || null,
    updatedAt: wiki.updatedAt || null
  }
}

function serializeSkillSummary(skill) {
  if (!skill) return skill
  return {
    name: skill.name,
    description: truncateText(skill.description || skill.content, 180),
    version: skill.version || null,
    enabled: skill.enabled !== false,
    priority: skill.priority || 0,
    category: skill.category || null,
    sourceType: skill.sourceType || null,
    sourceLabel: skill.sourceLabel || null,
    sourceScope: skill.sourceScope || null,
    availableToClaude: skill.availableToClaude !== false,
    isAutoGenerated: Boolean(skill.isAutoGenerated),
    createdAt: skill.createdAt || null,
    updatedAt: skill.updatedAt || null,
    sourcePath: skill.sourcePath || null
  }
}

async function ensureSkillsReady(ctx) {
  const reload = String(ctx.query.reload || '').toLowerCase() === 'true'
  if (reload) {
    await skills.reloadSkills()
  } else {
    await skills.initSkills()
  }
}

function enrichWikiForResponse(wiki) {
  if (!wiki || !wiki.parentTaskId) return wiki

  const hasStructuredContext =
    wiki.sourceSummary ||
    (Array.isArray(wiki.requirementHighlights) && wiki.requirementHighlights.length > 0) ||
    (Array.isArray(wiki.decisionHighlights) && wiki.decisionHighlights.length > 0) ||
    (Array.isArray(wiki.issueHighlights) && wiki.issueHighlights.length > 0) ||
    (Array.isArray(wiki.verificationHighlights) && wiki.verificationHighlights.length > 0)

  if (hasStructuredContext) return wiki

  const parentTask = db.getTaskById(wiki.parentTaskId)
  if (!parentTask) return wiki

  const context = buildWikiContext(parentTask, (taskId) => db.getTaskById(taskId), (taskId) => db.getTaskLogs(taskId))

  return {
    ...wiki,
    sourceSummary: context.sourceSummary,
    artifactPaths: context.artifactPaths,
    generatedFromTaskIds: context.generatedFromTaskIds,
    requirementHighlights: context.requirementHighlights,
    decisionHighlights: context.decisionHighlights,
    issueHighlights: context.issueHighlights,
    verificationHighlights: context.verificationHighlights
  }
}

