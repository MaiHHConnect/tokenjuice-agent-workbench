router.patch('/api/tasks/:id/status', async (ctx) => {
  const { id } = ctx.params
  const { status, operatorId, note } = ctx.request.body

  if (!status) {
    ctx.status = 400
    ctx.body = { error: 'status is required' }
    return
  }

  const result = db.updateTaskStatus(id, status, operatorId)
  if (!result) {
    ctx.status = 404
    ctx.body = { error: 'Task not found' }
    return
  }

  const { task, fromStatus } = result

  // 记录日志（测试通过/失败等操作记录）
  const actionLabels = {
    'ReadyForTest': '开发完成',
    'ReadyForDeploy': '测试通过',
    'InFix': '待修复',
    'Done': '任务完成',
    'InDev': '开始开发',
    'Collecting': '补充信息',
    'Analyzing': '开始分析'
  }
  const action = actionLabels[status] || '状态变更'
  db.addTaskLog(id, {
    agentId: operatorId || null,
    action: action,
    message: note || `${fromStatus} → ${status}`
  })

  if (['Done', 'ReadyForTest', 'InFix', 'Backlog'].includes(status)) {
    enhancedScheduler.requestImmediateSchedule(`task status updated ${fromStatus} -> ${status}`)
  }

  // 钉钉通知
  try {
    await dingtalk.notifyTaskStatusChanged(task, fromStatus, status, operatorId)
  } catch (e) {
    console.error('[Notify] Task status notification failed:', e.message)
  }

  ctx.body = { task: serializeTask(task) }
})

