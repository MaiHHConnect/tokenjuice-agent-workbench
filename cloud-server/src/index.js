router.post('/api/taskrunner/:taskId/start', async (ctx) => {
  const { taskId } = ctx.params
  const { steps = [], options = {} } = ctx.request.body

  // 检查任务是否存在
  const task = db.getTaskById(taskId)
  if (!task) {
    ctx.status = 404
    ctx.body = { error: 'Task not found' }
    return
  }

  // 创建运行器
  const runner = taskRunnerManager.create(taskId, options)

  // 设置步骤
  if (steps.length > 0) {
    runner.setSteps(steps)
  }

  // 默认步骤 (如果没有提供)
  if (runner.steps.length === 0) {
    runner.setSteps([
      { name: '开发', description: '执行开发任务', verifier: 'syntax' },
      { name: '编译', description: '编译检查', verifier: 'build' },
      { name: '测试', description: '运行测试', verifier: 'test' },
      { name: '风格检查', description: '代码风格检查', verifier: 'lint' }
    ])
  }

  // 执行器函数 (这里用模拟的，实际应该调用 AI)
  const executor = async (step, runner) => {
    return { success: true }
  }

  // 验证器函数
  const verifier = async (step, runner) => {
    const verifyFn = builtInVerifiers[step.verifier]
    if (verifyFn) {
      return await verifyFn(step, { runner })
    }
    return { passed: true, message: '跳过验证' }
  }

  // 开始执行
  const result = await runner.start(executor, verifier)

  ctx.body = {
    task: db.getTaskById(taskId),
    runner: runner.getStatus(),
    result
  }
})

