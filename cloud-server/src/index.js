router.post('/api/tasks/:id/import-artifact', async (ctx) => {
  const { id } = ctx.params
  const { sourcePath, targetPath = '' } = ctx.request.body || {}
  const task = db.getTaskById(id)

  if (!task) {
    ctx.status = 404
    ctx.body = { error: 'Task not found' }
    return
  }

  if (!sourcePath || !String(sourcePath).trim()) {
    ctx.status = 400
    ctx.body = { error: 'sourcePath is required' }
    return
  }

  try {
    const workspaceConfig = workflowManager.getWorkspaceConfig()
    const workspaceResult = await workspaceManager.createWorkspace(id, {
      afterCreate: workspaceConfig.hooks?.afterCreate
    })
    db.updateTaskWorkspace(id, {
      path: path.resolve(workspaceResult.path),
      status: task.status === 'Blocked' ? 'blocked' : (task.workspace?.status || 'retained_for_fix'),
      retainedForQa: false,
      cleanedAt: null
    })

    const artifact = workspaceManager.importArtifact(id, sourcePath, targetPath)
    enhancedScheduler.captureTaskArtifacts(id, workspaceResult.path)
    db.addTaskLog(id, {
      action: '人工导入工件',
      message: `${artifact.sourcePath} -> ${artifact.relativeDestinationPath}`
    })
    db.appendTaskOutput(id, `[系统] 已导入工件 ${artifact.relativeDestinationPath}`)

    ctx.body = {
      task: serializeTask(db.getTaskById(id)),
      artifact
    }
  } catch (error) {
    ctx.status = 400
    ctx.body = { error: error.message || 'Failed to import artifact' }
  }
})

